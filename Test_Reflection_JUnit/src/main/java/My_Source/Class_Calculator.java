package My_Source;

public class Class_Calculator {

	public int add(int a, int b) {
		return a + b;
	}

	public double add2(double a, double b) {
		return a + b;
	}
	
	public int sub(int a, int b) {
        return a - b;
    }

	public int multiply(int a, int b, int c) {
		return a * b * c;
	}

	public boolean isEven(int number) {
		return number % 2 == 0;
	}
}
